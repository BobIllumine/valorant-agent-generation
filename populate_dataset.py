import argparse
import os.path

import pandas as pd
import random

from scrapper import ValorantScrapper
from embeddings import Embedder
from character_generator import CharacterGenerator

roles = ['duelist', 'sentinel', 'controller', 'initiator', 'random']
sex = ['male', 'female', 'random']

parser = argparse.ArgumentParser(description='Utility for generating responses')
parser.add_argument('-C', '--country')
parser.add_argument('-R', '--role')
parser.add_argument('-S', '--sex')

args = parser.parse_args()

assert args.role.lower() in roles
assert args.country.lower() in [x[1].lower() for x in countries]
assert args.sex.lower() in sex

path_to_wiki = os.path.join('.', 'valorant_wiki')
if os.path.exists(path_to_wiki):
    scr = ValorantScrapper.from_saved(path_to_wiki)
else:
    scr = ValorantScrapper()
if os.path.exists(os.path.join(path_to_wiki, 'all.csv')):
    df = pd.read_csv(os.path.join(path_to_wiki, 'all.csv'))
else:
    df = scr.all_agents()

openai_key = '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'

emb = Embedder(openai_key)

if os.path.exists('embeddings.csv'):
    emb.load_embeddings('embeddings.csv')
else:
    emb.compute_doc_embeddings(df)

roles.remove('random')
countries.remove(('RNDM', 'Random'))
sex.remove('random')

